# Arithmetic Operators:

number1 <- 10
number2 <- 3

# Add
number1 + number2 # 13

# Subtract
number1 - number2 # 7

# Multiply
number1 * number2 # 30

# Divide (output = decimal real)
number1 / number2 # 3.3333333

# Modulo (ouput = remainder)
number1 %% number2 # 1

# Integer division (output = floor)
number1 %/% number2 # 3
